     function abrir(){ 
		document.getElementById('fondot').style.display="block"; 
		document.getElementById('contenido').style.display="block"; 
		document.getElementById('cierro').style.display="block"; 
	} 
	function cerrar(){ 
		document.getElementById('fondot').style.display="none"; 
		document.getElementById('contenido').style.display="none"; 
		document.getElementById('cierro').style.display="none"; 
	} 
